class Rune:
    def __init__(self, name):
        self.name = name
    
    def __repr__(self):
        return f"R-{self.name}"

FIRE = Rune("Fire")
WATER = Rune("Water")
EARTH = Rune("Earth")
WIND = Rune("Wind")

ICE = Rune("Ice")
GRASS = Rune("Grass")
ELECTRIC = Rune("Electric")

DARK = Rune("Dark")
LIGHT = Rune("Light")

SPACE = Rune("Space")
TIME = Rune("Time")

